# 1. operadores matematicos
numero_um = 20
numero_dois = 15

soma = numero_um + numero_dois
print(soma)

subtracao = numero_um - numero_dois
print(subtracao)

multiplicacao = numero_um * numero_dois
print(multiplicacao)

divisao = numero_um / numero_dois
print(divisao)

print(f"a soma entre {numero_um} e {numero_dois} resulta em {soma}")
print(f"a subtracao entre {numero_um} e {numero_dois} resulta em {subtracao}")
print(f"a multiplicacao entre {numero_um} e {numero_dois} resulta em {multiplicacao}")
print(f"a divisao entre {numero_um} e {numero_dois} resulta em {divisao}")
print("")